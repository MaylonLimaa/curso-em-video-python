"""
Curso Python #07 - Operadores Aritméticos

Ordem de procêdencia:
1 - ()
2 - **
3 - *, /, // e %
4 - + e -
"""

print(5+2)
# + é o operador de Soma
print(5-2)
# - é o operador de Subtração
print(5*2)
# * é o operador de Multiplicação
print(5/2)
# / é o operador de Divisão
print(5**2)
# ** é o operador de Potenciação
print(5//2)
# // é o operador de divisão inteira
print(5%2)
# % é o operador de módulo(resto da divisão)

print('----'*20)
# Exibe 20 vezes a mesma String
print(f'Olá {"Maylon":=^20}')
# Deixa o nome alinhado com 10 simbolos para cada lado
